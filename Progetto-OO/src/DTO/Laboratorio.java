package DTO;

public class Laboratorio {
	private String id_laboratorio;
	private int numero_lab;
	private String finalita_laboratorio;
	private String id_sede;
	
	
	public String getId_laboratorio() {
		return id_laboratorio;
	}
	public void setId_laboratorio(String id_laboratorio) {
		this.id_laboratorio = id_laboratorio;
	}
	public int getNumero_lab() {
		return numero_lab;
	}
	public void setNumero_lab(int numero_lab) {
		this.numero_lab = numero_lab;
	}
	public String getFinalita_laboratorio() {
		return finalita_laboratorio;
	}
	public void setFinalita_laboratorio(String finalita_laboratorio) {
		this.finalita_laboratorio = finalita_laboratorio;
	}
	public String getId_sede() {
		return id_sede;
	}
	public void setId_sede(String id_sede) {
		this.id_sede = id_sede;
	}
	

}
