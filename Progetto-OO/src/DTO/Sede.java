package DTO;

public class Sede {
	private String id_sede;
	private String nome_sede;
	private String indirizzo_sede;
	private String citta;
	private String numero_telefono;
	private String e_mail;
	private String e_mailPEC;
	private String id_centro;
	
	public String getId_sede() {
		return id_sede;
	}
	public void setId_sede(String id_sede) {
		this.id_sede = id_sede;
	}
	public String getNome_sede() {
		return nome_sede;
	}
	public void setNome_sede(String nome_sede) {
		this.nome_sede = nome_sede;
	}
	public String getIndirizzo_sede() {
		return indirizzo_sede;
	}
	public void setIndirizzo_sede(String indirizzo_sede) {
		this.indirizzo_sede = indirizzo_sede;
	}
	public String getCitta() {
		return citta;
	}
	public void setCitta(String citta) {
		this.citta = citta;
	}
	public String getNumero_telefono() {
		return numero_telefono;
	}
	public void setNumero_telefono(String numero_telefono) {
		this.numero_telefono = numero_telefono;
	}
	public String getE_mail() {
		return e_mail;
	}
	public void setE_mail(String e_mail) {
		this.e_mail = e_mail;
	}
	public String getE_mailPEC() {
		return e_mailPEC;
	}
	public void setE_mailPEC(String e_mailPEC) {
		this.e_mailPEC = e_mailPEC;
	}
	public String getId_centro() {
		return id_centro;
	}
	public void setId_centro(String id_centro) {
		this.id_centro = id_centro;
	}
	


}
